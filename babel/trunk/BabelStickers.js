function addStickers(stickerType, matrix) {
	for (var i in matrix) {
		if (matrix[i]) {
			globals.stickers.push(new Sticker(Math.floor(i / 15), i % 15, stickerType));
		}
	}

}



function Sticker(row, col, type) {
	this.row = row;
	this.col = col;
	this.type = type;
	this.image = new Image();
	
	this.image.hAlign = this.image.vAlign = 'center';
	this.image.opacity = 213;
	
	gridFrame.appendChild(this.image)
}



